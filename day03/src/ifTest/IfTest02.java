package ifTest;

public class IfTest02 {
	public static void main(String[] args) {
		//if~else문
		int num1 = 100;
		int num2 = 0;
		
		System.out.println("if~else");
		if (num1 > num2) {
			System.out.println("num1이 큽니다");
		}
//		System.out.println("첫번째 if문");
		else {
			System.out.println("num2가 큽니다");
		}
		
		System.out.println("if~else문 종료");
		
	}
}
